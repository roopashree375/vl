          <b><u>BUS Arbitration :</u><br></b>
            A conflict may arise if the number of DMA controllers or other controllers or processors try to access the common bus at the same time, but access can be given to only one of those. 
            Only one processor or controller can be Bus master at the same point in time.
            To resolve these conflicts, the Bus Arbitration procedure is implemented to coordinate the activities of all devices requesting memory transfers.
            The selection of the bus master must take into account the needs of various devices by establishing a priority system for gaining access to the bus. 
            The Bus Arbiter decides who would become the current bus master. <br><br>
         <span><b> There are two approaches to bus arbitration: </span></b><br><br>
          1.<u>Centralized bus arbitration</u> – A single bus arbiter performs the required arbitration. <br>
          2.<u>Distributed bus arbitration</u> – All devices participating in the selection of the next bus master. <br><br>
<b>There are three centralized bus arbitration methods:</b> <br>

(i)<u>Daisy Chaining method :–</u><br>
It is a simple and cheaper method where all the bus masters use the same line for making bus requests. The bus grant signal serially propagates through each master until it encounters the first one that is requesting access to the bus.<br>
<div class="GrayBlock">
            <br>
            <img id="theoryImage" src="../images/bus1.png">
            <br>
            </div><br>
(ii) <u>Polling or Rotating Priority method :–</u> <br>
In this, the controller is used to generate the address for the master(unique priority), the number of address lines required depends on the number of masters connected in the system. 
The controller generates a sequence of master addresses. When the requesting master recognizes its address, 
it activates the busy line and begins to use the bus.<br>
<div class="GrayBlock">
            <br>
            <img id="theoryImage" src="../images/bus2.png">
            <br>
            </div><br>
(iii)<u>Fixed priority or Independent Request method :– </u><br>
In this, each master has a separate pair of bus request and bus grant lines and each pair has a priority assigned to it.  
The built-in priority decoder within the controller selects the highest priority request and asserts the corresponding bus grant signal.<br>
