           <h3>Following are the basic operations on a BST.</h3>
              <div class="item"><strong>Create: </strong> creates an empty tree.</div>
              <div class="item"><strong>Search: </strong> Searches for a node in the tree.</div>
              <div class="item"><strong>Delete: </strong> deletes a node from the tree.</div>
              <div class="item"><strong>Inorder: </strong> in-order traversal of the tree.</div>
              <div class="item"><strong>Postorder: </strong> post-order traversal of the tree.</div>
