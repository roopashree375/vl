            <ul type="circle">
            <li>BSTs are used for a lot of applications due to its ordered structure.</li>
            <li>BSTs are used for indexing and multi-level indexing.</li>
            <li>They are also helpful to implement various searching algorithms.</li>
            <li>It is helpful in maintaining a sorted stream of data.</li>
