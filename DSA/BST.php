          Binary Search Tree is used to search elements efficiently and used as a collision handling technique in Hash Map implementations.Balanced Binary Search Tree is used to represent memory to enable fast memory allocation.<br><br>
          A Binary Search Tree (BST) is a tree in which all the nodes follow the below-mentioned properties <br>
          <ul type="circle">
          <li>The value of the key of the left sub-tree is less than the value of its parent (root) node's key.</li>

          <li>The value of the key of the right sub-tree is greater than or equal to the value of its parent (root) node's key.</li>

          <li>Thus, BST divides all its sub-trees into two segments; the left sub-tree and the right sub-tree and can be defined as −left_subtree (keys) < node (key) ≤ right_subtree (keys)</li>
