      <h3> Counters</h3><p>
      Counter is a device which stores the number of times a particular event or process has occurred, often in relationship to a clock signal. Counters are used in digital electronics for counting purpose, they can count specific event happening in the circuit. For example, in UP counter a counter increases count for every rising edge of clock. Not only counting, a counter can follow the certain sequence based on our design like any random sequence 0,1,3,2â€¦ .They can be designed with the help of flip flops.</p>
      Counters are broadly divided into two categories<br>
1. Asynchronous counter<br>
2. Synchronous counter
