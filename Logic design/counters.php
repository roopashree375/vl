              <div class="item"><strong>1. Asynchronous counter:</strong><p>The output obtained from the EX-OR gate is the sum of the two numbers while that obtained by AND gate is the carry. There will be no forwarding of carry addition because there is no logic gate to process that. Thus, this is called Half Adder circuit.</p>
              <p> It is evident from timing diagram that Q0 is changing as soon as the rising edge of clock pulse is 
encountered, Q1 is changing when rising edge of Q0 is encountered(because Q0 is like clock pulse 
for second flip flop) and so on. In this way ripples are generated through Q0,Q1,Q2,Q3 hence it is 
also called RIPPLE counter.
</p>
      <img id="theoryImage" src="images/asynch.png">
      <div class="item"><strong>2. Synchronous counter :</strong><p>Unlike the asynchronous counter, synchronous counter has one global clock which drives each flip 
flop so output changes in parallel. The one advantage of synchronous counter over asynchronous 
counter is, it can operate on higher frequency than asynchronous counter as it does not have 
cumulative delay because of same clock is given to each flip flop.</p>
</div>
