      <h3>Quine McCluskey Method</h3>
          In previous method, we discussed K-map, which is a convenient method for minimizing 
          Boolean functions up to 5 variables. But, it is difficult to simplify the Boolean functions having
          more than 5 variables by using this method.</p>
          <p>Quine-McCluskey method is a tabular method based on the concept of prime implicants. We 
          know that prime implicant is a product or sum term, which can’t be further reduced by 
          combining with any other product or sum terms of the given Boolean function.
          This tabular method is useful to get the prime implicants by repeatedly using the following 
          Boolean identity.</p>
          xy + xy’ = x(y+y′) = x.1 = x
